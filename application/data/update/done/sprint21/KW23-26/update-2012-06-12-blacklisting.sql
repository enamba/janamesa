DROP TABLE IF EXISTS `blacklist`;
CREATE TABLE `blacklist` ( 
    `id` INT UNSIGNED NOT NULL AUTO_INCREMENT PRIMARY KEY, 
    `adminId` INT UNSIGNED NOT NULL,
    `orderId` INT UNSIGNED NOT NULL,
    `comment` TEXT,
    `created` TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    `updated` TIMESTAMP NULL,
     INDEX `fkAdminId` (`adminId`),
     INDEX `fkOrderId` (`orderId`)
) ENGINE=InnoDB CHARACTER SET utf8 COLLATE utf8_general_ci;

DROP TABLE IF EXISTS `blacklist_values`;
CREATE TABLE `blacklist_values` ( 
    `id` INT UNSIGNED NOT NULL AUTO_INCREMENT PRIMARY KEY, 
    `blacklistId` INT UNSIGNED NOT NULL,
    `type` VARCHAR(255) NOT NULL,
    `value` VARCHAR(255) NOT NULL,
    `matching` VARCHAR(50) DEFAULT 'exact',
    `behaviour` VARCHAR(50) NOT NULL,
    `hits` INT UNSIGNED NOT NULL,
    `created` TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    `updated` TIMESTAMP NULL,
    `deleted` TINYINT NOT NULL,
    UNIQUE uqTypeValue (`type`,`value`),
    INDEX `fkBlacklistId` (`blacklistId`),
    INDEX `idxType` (`type`),
    INDEX `idxDeleted` (`deleted`)
) ENGINE=InnoDB CHARACTER SET utf8 COLLATE utf8_general_ci;

DROP TABLE IF EXISTS `blacklist_matching`;
CREATE TABLE `blacklist_matching` (
    `id` INT UNSIGNED NOT NULL AUTO_INCREMENT PRIMARY KEY,
    `valueId` INT UNSIGNED NOT NULL,
    `orderId` INT UNSIGNED NOT NULL,
